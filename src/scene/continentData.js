// Helper: read a cell from the bitmap (0 or 1), clamped at edges
function _get(col, row) {
  if (row < 0) row = 0;
  if (row >= ROWS) row = ROWS - 1;
  // Wrap columns for longitude continuity
  col = ((col % COLS) + COLS) % COLS;
  return MAP[row][col] === '1' ? 1.0 : 0.0;
}

/**
 * Sample the continent map with bilinear interpolation.
 * @param {number} u — longitude fraction, 0 = 180°W, 1 = 180°E (wraps)
 * @param {number} v — latitude fraction, 0 = 90°N, 1 = 90°S
 * @returns {number} 0.0 (ocean) to 1.0 (land)
 */
export function sampleContinent(u, v) {
  // Clamp v, wrap u
  u = u - Math.floor(u); // wrap to [0,1)
  v = Math.max(0, Math.min(1, v));

  const x = u * (COLS - 1);
  const y = v * (ROWS - 1);
  const ix = Math.floor(x);
  const iy = Math.floor(y);
  const fx = x - ix;
  const fy = y - iy;

  const s00 = _get(ix, iy);
  const s10 = _get(ix + 1, iy);
  const s01 = _get(ix, iy + 1);
  const s11 = _get(ix + 1, iy + 1);

  return s00 * (1 - fx) * (1 - fy) +
         s10 * fx * (1 - fy) +
         s01 * (1 - fx) * fy +
         s11 * fx * fy;
}
